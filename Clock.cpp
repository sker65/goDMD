Clock::Clock(LEDMatrixPanel& p, RTC_DS1307& rtc, SDClass* sd,
		DallasTemperature* sensor) :
		panel(p), nextClockRefresh(0), nextRtcSync(0), nextTempSync(0),
		sd(sd), sensor(sensor),
		actTemp(0.0) {
	this->rtc = &rtc;
	brightness = 0;
	active = false;
	mode = TIME;
	lastRtcSync = 0;
	digits = 0L;
}

Clock::~Clock() {
}

void Clock::readFont(File* f, Digit* p, int _size) {
	int j = 0;
	int size = _size;
	while(size-- > 0) {
		int ch = f->read(); // ignore char
		p[j].width = f->read();
		p[j].height = f->read();
		p[j].sizeInBytes = f->read()*256+f->read();
		DPRINTF("reading char '%c': w:%d, h:%d, bytes:%d\n",
				ch,p[j].width,p[j].height,p[j].sizeInBytes);
		// create struct for font data
		p[j].data = (byte*)malloc(p[j].sizeInBytes);
		f->read(p[j].data,p[j].sizeInBytes);
		j++;
	}
	// now read mask
	j = 0;
	size = f->read();
	if( _size != size ) {
		DPRINTF("size of mask does not match data!!\n");
	}
	while(size-- > 0) {
		f->read(); // ignore char
		f->seekCur(4); // skip dimensions
		p[j].mask = (byte*)malloc(p[j].sizeInBytes);
		f->read(p[j].mask,p[j].sizeInBytes);
		j++;
	}

}

boolean Clock::begin() {
	File f = sd->open("font.dat");
	if( !f ) return false;
	DPRINTF("reading font.dat\n");
	// size of map -> how many chars
	uint8_t size = f.read();
	DPRINTF("big font uses %d chars\n",size);
	digits = (Digit*)malloc(sizeof(Digit)*size);
	readFont(&f, digits, size);
	size = f.read();
	smallDigits = (Digit*)malloc(sizeof(Digit)*size);
	DPRINTF("small font uses %d chars\n",size);
	readFont(&f, smallDigits, size);
	f.close();
	return true;
}

void Clock::setMode( Mode newMode ) {
	if( this->mode != newMode ) panel.clearTime();
	this->mode = newMode;
}

void Clock::adjust(DateTime* dt) {
	rtc->adjust(*dt);
	nextRtcSync = millis(); // force sync
}

/**
 * updates the clock cyclic
 * @param now actual time in millis
 */
void Clock::update(long now) {
	if (nextRtcSync < now) {
		nextRtcSync = now + 30L * 60L * 1000L;  // 30 min
		n = rtc->now();
		lastRtcSync = now;
	}
	if (nextClockRefresh < now) {
		nextClockRefresh = now + 500;
		switch (mode) {
		case TIME:
		case TIMESEC:
			writeTime(now);
			break;
		case DATE:
			writeDate(now);
			break;
		case TEMP:
			if( nextTempSync < now ) {
				sensor->requestTemperaturesByIndex(0);
				actTemp= sensor->getTempCByIndex(0);
				nextTempSync = now + 20 * 1000L; // every 20 sec
			}
			writeTemp(actTemp);
			break;
		}
		// by forcing updates max bright is reached faster
		if (brightness < maxBrightness) {
			brightness++;
			nextClockRefresh = now + 200;
			panel.setTimeBrightness(brightness);
		}
	}
}

#define DIGITS "0123456789: .C°"

void Clock::writeDigit(int digit, int xoffset, uint8_t nBytes, byte* buffer) {
	if (buffer == NULL) {
		byte* src = digits[digit].data;
		for (int row = 0; row < 16; row++) {
			uint8_t* ptr = panel.getBuffers()[2] + row * panel.getWidth() / 4
					+ xoffset;
			for (int p = 0; p < nBytes; p++) {
				*ptr++ = *src++;
			}
		}
	} else {
		byte* src = digits[digit].mask;
		for (int row = 0; row < 16; row++) {
			uint8_t* p1 = buffer + row * panel.getWidth() / 4 + xoffset;
			for (int p = 0; p < nBytes; p++) {
				byte v = *p1;
				*p1++ = v | ~(*src++);
			}
		}
	}
}

void Clock::writeText(char* text, int offset, byte* buffer) {
	char*p = text;
	int x = offset;
	while(*p) {
		char* q = strchr(DIGITS,*p);
		if( q ) {
			// breite in bytes = digits[q-p].width / 4;
			writeDigit(q-p,x,4,buffer);
		}
		p++;
	}
}

int Clock::writeDoubleDigit(int digit, int x, byte* buffer) {
	if (digit >= 10) {
		writeDigit(digit / 10, x, 4, buffer);
	} else {
		writeDigit(0, x, 4, buffer);
	}
	x += 4;
	writeDigit(digit % 10, x, 4, buffer);
	x += 4;
	return x;
}

void Clock::writeTemp(float actTemp, byte* buffer) {
	char buf[7];
	sprintf(buf, "%03.1f °C",actTemp);
	DPRINTF("showing temp: %s\n",buf);
	writeText(buf,3,buffer);
}

void Clock::writeDate(long now, byte* buffer) {
	if (active) {
		int x = 3;
		DateTime dateTime(n.unixtime() + (now - lastRtcSync) / 1000);
		x = writeDoubleDigit(dateTime.day(),x,buffer);
		writeDigit(12, x, 2, buffer);
		x += 2;
		x = writeDoubleDigit(dateTime.month(),x,buffer);
		writeDigit(12, x, 2, buffer);
		x += 2;
		x = writeDoubleDigit(dateTime.year() % 1000,x,buffer);
	}
}

void Clock::writeTime(long now, byte* buffer) {
	if (active) {
		DateTime dateTime(n.unixtime() + (now - lastRtcSync) / 1000);
		boolean tick = (now % 1000) > 500;

		// choose offset
		int x = (mode==TIMESEC) ? 3 : 6; // for no seconds

		x = writeDoubleDigit(dateTime.hour(),x,buffer);
		// the colon
		writeDigit(tick ? 10 : 11, x, 2, buffer);
		x += 2;
		x = writeDoubleDigit(dateTime.minute(),x,buffer);
		if (mode==TIMESEC) {
		// the colon
			writeDigit(tick ? 10 : 11, x, 2, buffer);
			x += 2;
			x = writeDoubleDigit(dateTime.second(),x,buffer);
		}
	}
}

void Clock::clear() {
	byte* p = panel.getBuffers()[2];
	byte* pend = p + panel.getSizeOfBufferInByte();
	while (p < pend) {
		*p++ = 0xFF;
	}
}

void Clock::off() {
	if (active) {
		active = false;
		panel.setTimeBrightness(0);
		clear();
	}
}

void Clock::on() {
	if (!active) {
		active = true;
		brightness = 0;
		update(millis());
	}
}

DateTime& Clock::getActualTime() {
	DateTime p(n.unixtime() + (millis() - lastRtcSync) / 1000);
	return p;
}
